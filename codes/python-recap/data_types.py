#DATA TYPES
#Strings
greeting = "Hello"

print(greeting)

print(type(greeting))   

print(len(greeting))     #Length of string 

#Integers
age = 103.1

print(age)

print(type(age))

print(f"My age plus 5 is {age + 5}")          #Arithmetic operation  

#Floats
height = 5.9

print(height)

print(type(height))

print(f"My height plus 1.1 is {height + 1.1}")          #Arithmetic operation

#Booleans
is_student = True

print(is_student)

print(type(is_student))

print(f"Is the statement true? {is_student}")          #Boolean value
print(f"Is the statement false? {not is_student}")          #Boolean negation
print(f"Is the statement true? {is_student and False}")          #Boolean AND

